mystr="This is a test program"

print(mystr)

print(mystr[3]) # prints 3rd character of the string

# string slicing
print(mystr[0:4]) #to print "This" word

print(len(mystr))

print(mystr[0:23])

print(mystr[0:23:2])

print(mystr[0:len(mystr):3])

#Negative Index
print(mystr[-1:0])

print(mystr[-4:])

print(mystr[-4:-2])

print(mystr[::-1])

print(mystr.isalnum())

print(mystr.isalpha())

print(mystr.endswith("gram"))

print(mystr.count("o"))

print(mystr.capitalize()) # capitalizes the first letter

print(mystr.find("is"))

print(mystr.upper())

print(mystr.lower())

