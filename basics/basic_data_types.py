#Integer
a=25
print("Integer:",a,type(a))

#Float
b=25.5
print("Floating:",b,type(b))

#String
name = "text"
print("String:",name,type(name))

#Boolean
isBoolean = True
print("Boolean:",isBoolean,type(isBoolean))

#List(Ordered, Mutable)
numbers=[1,2,3,4]
print("List:",numbers,type(numbers))

#Tuple(Ordered, Immutable)
coordinates=(10,20)
print("Tuple:",coordinates,type(coordinates))

#Set (Unordered, No duplicates)
unique_numbers = {1,2,3,3}
print("Set:",unique_numbers,type(unique_numbers))

#Dictionary (Key value pair)
person={
    "name":"Max",
    "age":25
}
print("Dictionary:",person,type(person))